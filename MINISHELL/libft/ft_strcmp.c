#include "libft.h"

int	ft_strcmp(char *s, char *cmp)
{
	int	i;

	if (!s || s[0] == 0)
		return (-1);
	i = 0;
	while (s[i] && cmp[i])
	{
		if (s[i] != cmp[i])
			return (s[i] - cmp[i]);
		i++;
	}
	return (0);
}
